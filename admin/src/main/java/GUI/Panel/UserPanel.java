    public void dataTable(ArrayList<UserDTO> list) {

        DefaultTableModel model = new DefaultTableModel(
                new Object[][]{},
                new String[]{
    "ID", "Username", "Role", "Trạng thái"
}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (UserDTO u : list) {

    String status =
            Boolean.TRUE.equals(u.getEnabled())
            ? "Active"
            : "Banned";

    // ===== convert roles =====
    StringBuilder roleText =
            new StringBuilder();

    if (u.getRoles() != null) {

        for (RoleDTO role : u.getRoles()) {

            roleText.append(role.getRoleName())
                    .append(", ");
        }

        // xóa dấu phẩy cuối
        if (roleText.length() > 0) {

            roleText.setLength(
                    roleText.length() - 2
            );
        }
    }

    model.addRow(new Object[]{
        u.getId(),
        u.getUsername(),
        roleText.toString(),
        status
    });
}

        user_table.setModel(model);

        table_design.centerTable(user_table);
        table_design.setUpTable(user_table);

        TableColumnModel columnModel = user_table.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(50);
        columnModel.getColumn(1).setPreferredWidth(150);
        columnModel.getColumn(2).setPreferredWidth(100);
        columnModel.getColumn(3).setPreferredWidth(100);
    }

